//Para declarar un arreglo//
int[] numeros;

//Para inicializar un arreglo con tamaño fijo//
numeros = new int[5]; // arreglo con 5 elementos, inicializados a 0//

//Para asignar valores a posiciones específicas//
numeros[0] = 10;
numeros[1] = 20;

//También se puede declarar y asignar al mismo tiempo//
int[] numeros = new int[] { 1, 2, 3, 4, 5 };

//Leer valores de un arreglo//
//Se accede a un elemento usando su índice//
int valor = numeros[2]; // lee el tercer elemento, índice 2//